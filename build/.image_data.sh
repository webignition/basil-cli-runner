#!/usr/bin/env bash

BROWSER_IMAGE_PATTERN="smartassert/%s-runner"
CHROME_IMAGE_REPOSITORY=$(printf ${BROWSER_IMAGE_PATTERN} "chrome")
FIREFOX_IMAGE_REPOSITORY=$(printf ${BROWSER_IMAGE_PATTERN} "firefox")

DEFAULT_TAG="${TRAVIS_BRANCH:-master}"
TAG="${1:-${DEFAULT_TAG}}"

CHROME_IMAGE_NAME=${CHROME_IMAGE_REPOSITORY}:${TAG}
FIREFOX_IMAGE_NAME=${FIREFOX_IMAGE_REPOSITORY}:${TAG}

declare -a IMAGE_NAMES=(
  ${CHROME_IMAGE_NAME}
  ${FIREFOX_IMAGE_NAME}
)

echo "Images:"
for IMAGE_NAME in "${IMAGE_NAMES[@]}"; do
  echo "  "${IMAGE_NAME}
done
echo ""
